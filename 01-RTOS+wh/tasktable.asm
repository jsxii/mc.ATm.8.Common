;------------------------------------------------------------
;-- Empty cycle task, background ----------------------------
Idle:
	OUTI	MCUCR, 0x80
	SLEEP
	RET
;------------------------------------------------------------
TaskTable:
;	.dw	<Task Label> (=task addr)
	.dw	Idle	; [00] Idle
	.dw	OnLed3	; [01] TS_OnLed3
	.dw	OffLed3	; [02] TS_OffLed3
; etc...
;------------------------------------------------------------

;------------------------------------------------------------
.equ	TS_Idle = 0	; Task alias (const)
;.equ	TS_<Name> = <Index>
.equ	TS_OnLed3 = 1
.equ	TS_OffLed3 = 2
;------------------------------------------------------------
OnLed3:
	SBI	PORTD, 3
	SetTT	TS_OffLed3, 50
	RET
;------------------------------------------------------------
OffLed3:
	CBI	PORTD, 3
	SetTT	TS_OnLed3, 50
	RET
;------------------------------------------------------------
;------------------------------------------------------------


