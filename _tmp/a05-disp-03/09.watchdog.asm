WDToff:
WDR
IN	R16,WDTCR
ORI	R16,0x18	; WDCE = 1; WDE = 1;
OUT	WDTCR,R16
ANDI	R16,0xF7	; WDE = 0;
OUT	WDTCR,R16
RET
;------------------------------------------------------------
WDTon:
LDI	R16,0x00	; Timeout; 16384clk@1MHz * 2^n; n=0..7; (~16.3mS * 2^n); [n=7, timeout = ~2.1 sec]
ORI	R16,0x08	; WDE = 1
OUT	WDTCR,R16
RET
;------------------------------------------------------------
