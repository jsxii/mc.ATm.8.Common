;-- Initialisation --
Init:
		CLI
		mStackInit	; mac8

;-- Timer Init --

;-- Port Init --
		LDI		R16, 255
		OUT		DDRC, R16
		OUT		DDRD, R16
		OUT		PORTD, R16
		LDI		R16, 0
		OUT		PORTC, R16
		LDI		R16,1
		MOV		R15,R16
;-- LED Init --
		RCALL	InitLED
;		SEI

;== Main =========================================================
loop:
		LDI		R22, 0x30
		RCALL	int16
		RCALL	Wait
		LDI		R22, 0x31
		RCALL	int16
		RCALL	Wait
		LDI		R22, 0x32
		RCALL	int16
		RCALL	Wait
		LDI		R22, 0x33
		RCALL	int16
		RCALL	Wait
;------------------------------------------------------------
		RJMP	loop

;=================================================================
sendAll:
		RCALL	send0
		RCALL	send1
		RCALL	send2
		RCALL	send3
		RCALL	send4
		RCALL	send5
		RET
;------------------------------------------------------------
send0:
		CBI		PORTD, 0
		NOP
		NOP
		RCALL	sendWord
		NOP
		NOP
		SBI		PORTD, 0
		RET
;------------------------------------------------------------
