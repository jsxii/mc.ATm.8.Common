		NOP
		RCALL	oneBit
		NOP
		RCALL	oneBit
		NOP
		RCALL	oneBit
		NOP
		RCALL	oneBit
		NOP
		RCALL	oneBit
		NOP
		RCALL	oneBit
		NOP
		RCALL	oneBit
		NOP
		NOP
		NOP
		NOP
		NOP
		CBI		PORTC, 1
		NOP
		NOP
		NOP
		NOP
		RET
;------------------------------------------------------------
oneBit:
		;RCALL				; 3
		ROL		R16			; 1
		ROL		R17			; 1
		CBI		PORTC,1		; x
		AND		R17,R15		; 1
		OUT		PORTC,R17	; 1
		NOP					; 1 1
		NOP					; 1 2
		NOP					; 1 3
		NOP					; 1 4
		NOP					; 1 5
		NOP					; 1 6
		NOP					; 1 7
		SBI		PORTC,1		; x
		RET					; 4
;------------------------------------------------------------
wait:
		RCALL	wait0
		LDI		R16,128
		RJMP	w1
wait0:
		LDI		R16,255
w1:
		LDI		R17,255
w2:
		NOP
		NOP
		NOP
		NOP
		DEC		R17
		BRNE	w2
		DEC		R16
		BRNE	w1
		RET
;------------------------------------------------------------
InitLED:
		LDI		R19, 9
		LDI		R20, 0
		RCALL	sendAll
		LDI		R19, 10
		LDI		R20, 7
		RCALL	sendAll
		LDI		R19, 11
		RCALL	sendAll
		LDI		R19, 12
		LDI		R20, 15
		RCALL	sendAll
		RET
;------------------------------------------------------------
int16:	; R22 - code; R21 - pos(0..5)
;Z = SymGen + R22 * 8
		LDI		R23, 0
		CLC
		ROL		R22
		ROL		R23
		ROL		R22
		ROL		R23
		ROL		R22
		ROL		R23
		LDI		ZL, low(SymGen)
		LDI		ZH, high(SymGen)
		ADD		ZL, R22
		ADC		ZH, R23
;-- position All
		LDI		R19, 1
		LDI	R20,1	;LPM		R20,Z+
		RCALL	sendAll
		LDI		R19, 2
		LDI	R20,1	;LPM		R20,Z+
		RCALL	sendAll
		LDI		R19, 3
		LDI	R20,1	;LPM		R20,Z+
		RCALL	sendAll
		LDI		R19, 4
		LDI	R20,1	;LPM		R20,Z+
		RCALL	sendAll
		LDI		R19, 5
		LDI	R20,1	;LPM		R20,Z+
		RCALL	sendAll
		LDI		R19, 6
		LDI	R20,1	;LPM		R20,Z+
		RCALL	sendAll
		LDI		R19, 7
		LDI	R20,1	;LPM		R20,Z+
		RCALL	sendAll
		LDI		R19, 8
		LDI	R20,1	;LPM		R20,Z+
		RCALL	sendAll
		RET
